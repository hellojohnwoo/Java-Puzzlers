package character;

public class Puzzle011LastLaugh {
    public static void main(String[] args) {
        System.out.println("H" + "a");
        System.out.println("H" + "A");

        System.out.println('H' + 'a');
        System.out.println('H' + 'A');


        System.out.println();
        System.out.println("" + 'H' + 'a');
        System.out.println("2 + 2 = " + 2+2);

        System.out.println();
        System.out.printf("%c%c", 'H', 'a');            // java5 version

        System.out.println();
        System.out.println(String.valueOf(2) + '2');    //  StringBuffer Class
    }
}
